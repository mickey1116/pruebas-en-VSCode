def convertirADecimal(cadena_hexadecimal):
    cadena_decimal=(cadena_hexadecimal,16)
    cadena_decimal=str(int(cadena_hexadecimal))
    return cadena_decimal


def convertirAOctal(cadena_hexadecimal):
    cadena_octal=(cadena_hexadecimal,16)
    cadena_octal=str(oct(cadena_hexadecimal))
    return cadena_octal


def convertirABinario(cadena_hexadecimal):
    cadena_binario=(cadena_hexadecimal,16)
    cadena_binario=str(bin(cadena_hexadecimal))
    return cadena_binario


