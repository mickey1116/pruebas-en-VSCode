def convertirABinario(cadena_decimal):
    cadena_binario=float(cadena_decimal)
    cadena_binario=str(bin(cadena_binario))
    return cadena_binario


def convertirAOctal(cadena_decimal):
    cadena_octal=float(cadena_decimal)
    cadena_octal=str(oct(cadena_decimal))
    return cadena_octal


def convertirAHexadecimal(cadena_decimal):
    cadena_hexadecimal=float(cadena_decimal)
    cadena_hexadecimal=str(hex(cadena_decimal))
    return cadena_hexadecimal


