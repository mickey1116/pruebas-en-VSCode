def convertirADecimal(cadena_binario):
    cadena_decimal=(cadena_binario,2)
    cadena_decimal=str(int(cadena_decimal))
    return cadena_decimal


def convertirAOctal(cadena_binario):
    cadena_octal=(cadena_binario,2)
    cadena_octal=str(oct(cadena_binario))
    return cadena_octal


def convertirAHexadecimal(cadena_binario):
    cadena_hexadecimal=(cadena_binario,2)
    cadena_hexadecimal=str(hex(cadena_binario))
    return cadena_hexadecimal


