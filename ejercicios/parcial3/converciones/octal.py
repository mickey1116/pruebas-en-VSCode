def convertirADecimal(cadena_octal):
    cadena_decimal=(cadena_octal,8)
    cadena_decimal=str(int(cadena_octal))
    return cadena_decimal


def convertirABinario(cadena_octal):
    cadena_binario=(cadena_octal,8)
    cadena_binario=str(bin(cadena_octal))
    return cadena_binario


def convertirAHexadecimal(cadena_octal):
    cadena_hexadecimal=(cadena_octal,8)
    cadena_hexadecimal=str(hex(cadena_octal))
    return cadena_hexadecimal


